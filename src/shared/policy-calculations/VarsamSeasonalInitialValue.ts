    public static getInitialValuePremium(
        Product: string,
        CoverType: string,
        Age: number,
        Hussopp: boolean,
        InnboStandard: boolean,
        InnboSuper: boolean
    ) {
        let result = VarsamSeasonalInitialValue.initialValuePremium.filter(
            e => e.Product === Product && e.CoverType === CoverType && e.Hussopp === Hussopp && e.InnboStandard === InnboStandard && e.InnboSuper === InnboSuper
        );
        if(Age >= 36){
            return result.filter(e => e.Age === 36)[0];
        }
        else{
            return result.filter(e => e.Age === Age)[0];
        }
    }

    public static getInitialValueCoverBreakdown(
        Product: string,
        Age: number
    ){
        let temp = VarsamSeasonalInitialValue.initialValueCoverBreakdown.filter(e => e.Product === Product);
        if(Age >= 36){
            return temp.filter(e => e.Age === 36)[0];
        }
        else{
            return temp.filter(e => e.Age === Age)[0];
        }
    }
